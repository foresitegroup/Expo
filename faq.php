<h1>FAQ</h1>

There are usually a lot of questions when preparing for a trade show and getting your display ready for presentation. Some of the questions are asked and answered in this section. If you have a question that is not covered in our FAQs feel free to call us at 414-727-9456. A representative  of Expo Milwaukee will answer your questions.<br>
<br>

<strong>Can one person set up a Portable Display?</strong><br>
Yes. Our  Displays are designed to be set-up quickly and easily by one person.<br>
<br>

<strong>Can I take my Display on the airline as luggage?</strong><br>
Yes, you can check most 10' and smaller  Displays in as luggage. Our cases fit within the limits required by most of the airlines, although due to the size and/or weight, you may be charged an oversize luggage fee. (Please check with your airline to verify the restrictions).<br>
<br>

<strong>How do I attach graphic panels to my Display?</strong><br>
Our Displays come standard with Velcro-acceptable fabric. You can simply Velcro attach your graphic panels anywhere on the display. Or, you can elect to use full Mural Graphics which are designed to take the place of one or more of the fabric panels and would run the full height of the Display.<br>
<br>

<strong>Are tabletop displays easy to travel with?</strong><br>
Yes. They set up and dismantle in minutes, using no tools. Just fold and transport in the carrying bag (if purchased). They are lightweight and easily fit in the trunk of your car, or even the back seat. Many units can also be taken on airlines as a carry on (depending on model) or checked in as luggage (recommended only if a hard shipping case is purchased).<br>
<br>

<strong>Can I add lighting to my tabletop display?</strong><br>
Yes. Lighting can be ordered for all folding tabletop displays.<br>
<br>

<strong>I received my display with a component missing.</strong><br>
Upon receipt of your display, we recommend that all of our customers check the parts list to make sure your order is complete. You have 72 hours from the receipt of your Displays to report any deficiencies. Should there be a missing component, all you need to do is contact your Sales Representative and he/she will make sure that the missing component will be shipped immediately.

