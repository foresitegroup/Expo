<h1>Graphic Specs</h1>

On the fast-paced trade show floor, you only have seconds to gain your prospect's interest, so first impressions are critical. Often a prospect's only impression of your company is what he or she sees at the show. Let Expo Milwaukee help you design and print graphics and signs that project your company's image with impact, leaving a lasting impression on those who matter most-your future customers.<br>
<br>
<br>

<strong>Acceptable Graphics Formats</strong><br>
<ul>
  <li>Send a color proof (printed proof or JPG/PDF) of your artwork along with the digital file.</li>
  <li>Enclose full contact info with your media: job name, job number, company name, contact name, account rep, phone numbers, email.</li>
  <li>Our graphics department is Mac-based. We can accept PC-generated files also.</li>
  <li>Bitmap graphics (photos) must be 100 dpi at full size, 200 dpi at half size, or scaled as needed to make the final image meet 100 dpi criteria, save in RC.</li>
  <li>We will provide printed proofs at client's request, pricing for proofs varies &ndash; please call.</li>
</ul>
<br>

<strong>Acceptable Media Formats (Mac or PC)</strong><br>
<ul>
  <li>CD/DVD</li>
  <li>E-Mail <?php email("mark@expomilwaukee.com"); ?></li>
  <li>Photo Panels &ndash; Please build as one single image (even if graphic is four or more panels). Do not use bleed.</li>
  <li>Velcro Graphics &ndash; Do not use any bleed.</li>
  <li>Sintra Plex/Foam Graphics &ndash; Please build with 0.25" bleed on length &amp; width.</li>
  <li>Banners &ndash; Please call to request banner templates.</li>
</ul>
<br>

<strong>Acceptable Graphic Formats</strong><br>
Adobe PhotoShop CS5 (Preferred): All standard inkjet graphics are printed as TIFF files. Save your flattened file in TIFF format in RGB mode. <strong>Photoshop Checklist: RGB mode / Flattened / 100 dpi at full size / TIFF or HI-Res JPG.</strong><br>
<br>

Adobe Illustrator CS5 - Illustrator is ideal for creating vector files - required for all vinyl, CNC and fabric jobs. All fonts must be converted to outlines.
<strong>Illustrator Checklist: RGB Mode / Outline fonts / placed image links, not embedded / send separate image files.</strong><br>
<br>

Adobe InDesign CS5: InDesign files can and should be created at final print size. Fonts converted to outlines.
<strong>InDesign Checklist: RGB Mode / Outline Fonts, Placed image links / separate image file.</strong><br>
<br>

<strong>Unacceptable File Formats</strong><br>
<ul>
  <li>Microsoft Office Products (Word, Excel, PowerPoint, Publisher). These programs do not readily support large-format output. Call with any questions.</li>
  <li>Low-resolution JPEG, GIF, PICT, or BMP images (typical e-mail/web site images). These files are low resolution and small size are too small for output.</li>
  <li>DXF files: AutoCAD files will not convert cleanly. Call with questions about AutoCAD conversion.</li>
</ul>
